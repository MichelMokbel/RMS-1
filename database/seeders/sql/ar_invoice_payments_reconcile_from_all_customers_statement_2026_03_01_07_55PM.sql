  ai.status = CASE
    WHEN m.normalized_balance_cents = 0 THEN 'paid'
    WHEN m.normalized_paid_cents > 0 THEN 'partially_paid'
    ELSE 'issued'
  END,
  ai.updated_at = NOW();
SET @statement_override_updates := ROW_COUNT();

